export function t(lang: string | undefined, key: string): string {
  const l = lang && strings[lang] ? lang : 'sr'
  return strings[l][key] ?? strings['sr'][key] ?? key
}

export function tCat(lang: string | undefined, categoryId: string | undefined): string {
  if (!categoryId) return ''
  return t(lang, `cat_${categoryId.toLowerCase()}`)
}
